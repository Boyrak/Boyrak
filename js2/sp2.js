var checkboxesClicked = {};

function toggleCheckboxAndCall(checkbox, functionName) {
    if (!checkboxesClicked[functionName]) {
        window[functionName]();
        checkboxesClicked[functionName] = true;
    }
    checkbox.checked = !checkbox.checked;
}

